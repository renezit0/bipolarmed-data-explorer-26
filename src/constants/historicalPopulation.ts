// Função para obter população de um estado em um ano específico
export const getStatePopulation = (stateCode: string, year: number): number => {
  const statePop = HISTORICAL_POPULATION_BY_STATE[stateCode.toLowerCase()];
  if (!statePop) return 0;
  return statePop[year] || statePop[2024]; // fallback para 2024 se ano não existir
};

// Função para obter população de uma região em um ano específico
export const getRegionPopulation = (regionName: string, year: number): number => {
  const regionPop = HISTORICAL_POPULATION_BY_REGION[regionName];
  if (!regionPop) return 0;
  return regionPop[year] || regionPop[2024]; // fallback para 2024 se ano não existir
};

// Função para extrair ano de um período (formato "YYYY/Mês")
export const extractYearFromPeriod = (period: string): number => {
  const yearMatch = period.match(/^(\d{4})/);
  return yearMatch ? parseInt(yearMatch[1]) : 2024;
};
